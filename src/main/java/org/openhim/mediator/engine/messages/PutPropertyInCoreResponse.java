package org.openhim.mediator.engine.messages;

public class PutPropertyInCoreResponse {
    private final String name;
    private final String value;

    public PutPropertyInCoreResponse(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
